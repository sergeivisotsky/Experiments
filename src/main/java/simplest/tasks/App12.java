package simplest.tasks;

public class App12 {
    public static void main(String args[]) {
        int m[] = {-5, -4, -3, -2, -1, 0, 1, 2, 3, 4};
        for (int i = 0; i < 10; i++) {
            if (m[i]>0) {
                System.out.print(m[i] = 1);
            } else {
                System.out.print(m[i] = 2);
            }
        }
    }
}
